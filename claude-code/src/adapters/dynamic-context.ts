import type { DynamicContextIR, HarnessType } from '../ir.js';

/**
 * Dynamic context support per harness.
 */
export const DYNAMIC_CONTEXT_SUPPORT: Record<HarnessType, {
  native: boolean;
  syntax: string | null;
  workaround: string;
}> = {
  claude:   { native: true,  syntax: '!`command`',    workaround: 'N/A — native support' },
  cursor:   { native: false, syntax: null,             workaround: 'Generate preprocessor script that renders SKILL.md before use' },
  codex:    { native: false, syntax: null,             workaround: 'Generate wrapper script that renders AGENTS.md before codex launch' },
  openclaw: { native: false, syntax: null,             workaround: 'Convert to preamble bash block (clearshot pattern)' },
  copilot:  { native: false, syntax: null,             workaround: 'Not feasible — drop with annotation' },
  windsurf: { native: false, syntax: null,             workaround: 'Not feasible — drop with annotation' },
};

/**
 * Generate a preamble bash block for dynamic context (OpenClaw/clearshot pattern).
 */
export function generatePreamble(contexts: DynamicContextIR[]): string {
  const lines = contexts.map((ctx) => {
    const varName = ctx.command.replace(/[^a-zA-Z0-9_]/g, '_').toUpperCase();
    return `${varName}=$(${ctx.command} 2>/dev/null || echo "[unavailable]")\necho "${ctx.placeholder}: $${varName}"`;
  });

  return `## Preamble\n\nRun this bash block first:\n\n\`\`\`bash\n${lines.join('\n\n')}\n\`\`\``;
}

/**
 * Generate a preprocessor shell script for dynamic context rendering.
 */
export function generatePreprocessorScript(contexts: DynamicContextIR[]): string {
  const lines = [
    '#!/bin/bash',
    '# Generated by skillport — renders dynamic context placeholders',
    '',
    'SKILL_DIR="$(cd "$(dirname "$0")/.." && pwd)"',
    'SKILL_FILE="$SKILL_DIR/SKILL.md"',
    '',
  ];

  for (const ctx of contexts) {
    const escaped = ctx.placeholder.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    lines.push(`# ${ctx.placeholder}`);
    lines.push(`RESULT=$(${ctx.command} 2>/dev/null || echo "[command failed]")`);
    lines.push(`sed -i '' "s|${escaped}|$RESULT|g" "$SKILL_FILE"`);
    lines.push('');
  }

  lines.push('echo "Dynamic context rendered"');
  return lines.join('\n');
}
